-- ── TABLE DEFINITIONS ────────────────────────────────────────

CREATE TABLE IF NOT EXISTS users (
    username        TEXT PRIMARY KEY,
    password_hash   TEXT NOT NULL,
    role            TEXT NOT NULL CHECK (role IN ('admin','vendor','customer','warehouse')),
    display_name    TEXT,
    company_name    TEXT,
    email           TEXT,
    warehouse_role  TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS orders (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_number        TEXT UNIQUE NOT NULL,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL REFERENCES users(username),
    items               JSONB NOT NULL DEFAULT '[]',
    status              TEXT NOT NULL DEFAULT 'requested',
    undelivered_reason  TEXT,
    requested_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS claims (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    claim_number        TEXT UNIQUE NOT NULL,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL REFERENCES users(username),
    order_id            BIGINT REFERENCES orders(id),
    sku                 TEXT NOT NULL,
    damage_type         TEXT,
    damaged_qty         INT DEFAULT 0,
    claim_text          TEXT,
    status              TEXT NOT NULL DEFAULT 'pending',
    decision_reason     TEXT,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- NOTE: vendor_inventory intentionally has no FK on vendor_username
-- because the seed data references placeholder vendors (vendorx/y/z)
-- not present in users. Real vendor rows will reference users correctly.
CREATE TABLE IF NOT EXISTS vendor_inventory (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendor_username         TEXT NOT NULL,
    sku                     TEXT NOT NULL,
    item_name               TEXT,
    qty_on_hand             INT NOT NULL DEFAULT 0,
    reorder_threshold       INT NOT NULL DEFAULT 0,
    manufacturing_critical  BOOLEAN NOT NULL DEFAULT FALSE,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (vendor_username, sku)
);

CREATE TABLE IF NOT EXISTS customer_inventory (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL,
    sku                 TEXT NOT NULL,
    item_name           TEXT,
    qty_on_hand         INT NOT NULL DEFAULT 0,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (customer_username, vendor_username, sku)
);

CREATE TABLE IF NOT EXISTS customer_vendor_links (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL REFERENCES users(username),
    linked_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (customer_username, vendor_username)
);

CREATE TABLE IF NOT EXISTS vendor_sla (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendor_username         TEXT NOT NULL REFERENCES users(username),
    customer_usernames      TEXT[] NOT NULL DEFAULT '{}',
    sla_document_filename   TEXT,
    sla_text_cache          TEXT,
    liability_summary       TEXT,
    uploaded_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS alerts (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    audience         TEXT NOT NULL,
    target_username  TEXT,
    type             TEXT NOT NULL,
    title            TEXT,
    message          TEXT,
    related_id       BIGINT,
    status           TEXT NOT NULL DEFAULT 'unread',
    created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS audit_logs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    timestamp   TIMESTAMPTZ NOT NULL DEFAULT now(),
    actor       TEXT,
    action      TEXT,
    module      TEXT,
    record_id   BIGINT,
    details     TEXT
);

CREATE TABLE IF NOT EXISTS purchase_orders (
    id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    po_number       TEXT,
    vendor_username TEXT NOT NULL REFERENCES users(username),
    sku             TEXT,
    item_name       TEXT,
    quantity        INT DEFAULT 1,
    created_by      TEXT,
    date_raised     TIMESTAMPTZ NOT NULL DEFAULT now(),
    status          TEXT NOT NULL DEFAULT 'Pending',
    delivery_date   TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

