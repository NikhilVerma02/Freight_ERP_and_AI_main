-- ── SAFETY: drop everything in reverse dependency order ───────────────────
DROP TABLE IF EXISTS audit_logs          CASCADE;
DROP TABLE IF EXISTS alerts              CASCADE;
DROP TABLE IF EXISTS sales_orders        CASCADE;
DROP TABLE IF EXISTS purchase_orders     CASCADE;
DROP TABLE IF EXISTS vendor_sla          CASCADE;
DROP TABLE IF EXISTS customer_vendor_links CASCADE;
DROP TABLE IF EXISTS customer_inventory  CASCADE;
DROP TABLE IF EXISTS vendor_inventory    CASCADE;
DROP TABLE IF EXISTS claims              CASCADE;
DROP TABLE IF EXISTS orders              CASCADE;
DROP TABLE IF EXISTS users               CASCADE;

-- ── 1. USERS ─────────────────────────────────────────────────────────────
CREATE TABLE users (
    username        TEXT PRIMARY KEY,
    password_hash   TEXT NOT NULL,
    role            TEXT NOT NULL CHECK (role IN (
                        'admin',
                        'procurement_officer',
                        'inventory_controller',
                        'finance_officer',
                        'vendor_order_manager',
                        'vendor_claim_handler',
                        'customer'
                    )),
    display_name    TEXT,
    company_name    TEXT,
    email           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ── 2. ORDERS ─────────────────────────────────────────────────────────────
CREATE TABLE orders (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_number        TEXT UNIQUE NOT NULL,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL REFERENCES users(username),
    items               JSONB NOT NULL DEFAULT '[]',
    status              TEXT NOT NULL DEFAULT 'requested',
    undelivered_reason  TEXT,
    requested_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ── 3. CLAIMS ─────────────────────────────────────────────────────────────
CREATE TABLE claims (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    claim_number        TEXT UNIQUE NOT NULL,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL REFERENCES users(username),
    order_id            BIGINT REFERENCES orders(id),
    sku                 TEXT NOT NULL,
    damage_type         TEXT,
    damaged_qty         INT DEFAULT 0,
    claim_text          TEXT,
    status              TEXT NOT NULL DEFAULT 'pending',
    decision_reason     TEXT,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ── 4. VENDOR INVENTORY ───────────────────────────────────────────────────
-- No FK on vendor_username — allows placeholder/seed vendors not in users table
CREATE TABLE vendor_inventory (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendor_username         TEXT NOT NULL,
    sku                     TEXT NOT NULL,
    item_name               TEXT,
    qty_on_hand             INT NOT NULL DEFAULT 0,
    reorder_threshold       INT NOT NULL DEFAULT 0,
    manufacturing_critical  BOOLEAN NOT NULL DEFAULT FALSE,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (vendor_username, sku)
);

-- ── 5. CUSTOMER INVENTORY ─────────────────────────────────────────────────
CREATE TABLE customer_inventory (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL,
    sku                 TEXT NOT NULL,
    item_name           TEXT,
    qty_on_hand         INT NOT NULL DEFAULT 0,
    unit_price          NUMERIC DEFAULT 0,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (customer_username, vendor_username, sku)
);

-- ── 6. CUSTOMER-VENDOR LINKS ──────────────────────────────────────────────
CREATE TABLE customer_vendor_links (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_username   TEXT NOT NULL REFERENCES users(username),
    vendor_username     TEXT NOT NULL REFERENCES users(username),
    linked_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (customer_username, vendor_username)
);

-- ── 7. VENDOR SLA DOCUMENTS ───────────────────────────────────────────────
CREATE TABLE vendor_sla (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendor_username         TEXT NOT NULL REFERENCES users(username),
    customer_usernames      TEXT[] NOT NULL DEFAULT '{}',
    sla_document_filename   TEXT,
    sla_text_cache          TEXT,
    liability_summary       TEXT,
    uploaded_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ── 8. ALERTS ─────────────────────────────────────────────────────────────
CREATE TABLE alerts (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    audience         TEXT NOT NULL,
    target_username  TEXT,
    type             TEXT NOT NULL,
    title            TEXT,
    message          TEXT,
    related_id       BIGINT,
    status           TEXT NOT NULL DEFAULT 'unread',
    created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ── 9. AUDIT LOGS ─────────────────────────────────────────────────────────
CREATE TABLE audit_logs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    timestamp   TIMESTAMPTZ NOT NULL DEFAULT now(),
    actor       TEXT,
    action      TEXT,
    module      TEXT,
    record_id   BIGINT,
    details     TEXT
);

-- ── 10. PURCHASE ORDERS ───────────────────────────────────────────────────
CREATE TABLE purchase_orders (
    id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    po_number       TEXT UNIQUE,
    vendor_username TEXT NOT NULL REFERENCES users(username),
    sku             TEXT,
    item_name       TEXT,
    item_code       TEXT,
    item_quantity   INT DEFAULT 1,
    quantity        INT DEFAULT 1,
    customer_name   TEXT,
    total_cost      NUMERIC DEFAULT 0,
    created_by      TEXT,
    so_number       TEXT,
    date_raised     TIMESTAMPTZ NOT NULL DEFAULT now(),
    status          TEXT NOT NULL DEFAULT 'Pending',
    delivery_date   TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ── 11. SALES ORDERS ──────────────────────────────────────────────────────
CREATE TABLE sales_orders (
    id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    so_number       TEXT UNIQUE NOT NULL,
    po_id           UUID NOT NULL REFERENCES purchase_orders(id),
    po_number       TEXT,
    vendor_username TEXT NOT NULL REFERENCES users(username),
    dispatched_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    delivered_at    TIMESTAMPTZ,
    status          TEXT NOT NULL DEFAULT 'Dispatched',
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

